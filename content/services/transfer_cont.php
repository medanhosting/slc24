<h2><span>Introduction</span></h2>
<p>You are an online merchant or run a site offering services and you want to accept Solidcoins as a payment option? Or you want to make payouts in Solidcoins? You only have PHP webspace or don't want to mess with virtual servers? We can help you! <a href="?c=services/transfer">Solidcoin24 Transfer</a> allows you to easily accept or pay out Solidcoins via PHP with just a few lines of code. And there isn't even a way for your users to find out that you're using a foreign service. That means maximal flexibility to you.</p>
